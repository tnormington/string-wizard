export function getPatternsByGenre(genre: string): RhythmPattern[] {
  return rhythmPatterns.filter(p => p.genre.includes(genre as RhythmPattern['genre'][number]));
}

export function getPatternsByDifficulty(difficulty: string): RhythmPattern[] {
  return rhythmPatterns.filter(p => p.difficulty === difficulty);
}

export function getPatternsByTimeSignature(timeSig: string): RhythmPattern[] {
  return rhythmPatterns.filter(p => p.timeSignature === timeSig);
}

export function getPatternsByType(type: string): RhythmPattern[] {
  return rhythmPatterns.filter(p => p.type === type);
}
